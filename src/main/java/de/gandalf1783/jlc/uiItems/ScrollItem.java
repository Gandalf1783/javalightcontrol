package de.gandalf1783.jlc.uiItems;

import java.awt.*;

public class ScrollItem extends UiItem {

    public ScrollItem(int x, int y, Graphics g) {
        super(x, y, g);
    }

    @Override
    public void render(Graphics g) {
        this.render(g);
    }

    public void tick() {
        this.tick();
    }

}
